        // Hiển thị hoặc ẩn nút cuộn lên đầu trang
        $(window).scroll(function(){
            if ($(this).scrollTop() > 100) {
                $('#scrollToTopBtn').fadeIn();
            } else {
                $('#scrollToTopBtn').fadeOut();
            }
        });

        // Cuộn lên đầu trang khi nút được nhấp
